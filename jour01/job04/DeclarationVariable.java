package job04;

public class DeclarationVariable {
    public static void main(String[] args) {
        /*  
            * Declare 3 variables of type int on a single line and
            * assign values to the variables in the same line
            * The variables are a, b, and c, and their values are 0
        */
        int a =0, b=0, c=0;
        // Print the values of the variables
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
    }
}
